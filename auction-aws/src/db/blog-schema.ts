import {
    attribute,
    autoGeneratedHashKey,
    rangeKey,
    table,
} from '@aws/dynamodb-data-mapper-annotations';

import { dateMarshall, dateUnmarshall } from "./date-dynamo";

@table("Blog")
export class Blog {
    @autoGeneratedHashKey()
    blogId?: string;

    @attribute()
    title?: string;

    @attribute()
    content?: string;

    @attribute()
    userId?: string;

    @attribute({ marshall: dateMarshall, unmarshall: dateUnmarshall })
    created_at?: Date;
  
    @attribute({ marshall: dateMarshall, unmarshall: dateUnmarshall })
    updated_at?: Date;

    @attribute()
  deleted?: boolean;
}